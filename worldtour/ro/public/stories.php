				<h1 class="content_headers">Bella Taormina!</h1>
				<span class="postedby" id="postedby">
					Postat de <a href="">administrator</a> la <span class="time">time</span>
				</span>
				<article>
					Ne-am hotarat sa vizitam Italia in luna septembrie si pentru ca cel mai bun loc care descrie cultura acestei tari e Sicilia, am inceput sa cautam cazare si bilete de avion.  <br><br>
					Am descoperit intre timp ca asupra acestei insule si-au lasat influenta de-a lungul timpului mai multe civilizatii importante, printre care grecii, romanii, turcii si arabii, acest lucru fiind vizibil din arhitectura cladirilor. <br><br>
					Cand ne-am planificat cazarea , am incercat sa inchiriem ceva cat mai aproape de aeroportul Fontanarossa, dar preturile fiind peste posibilitatile noastre, am decis sa optam pentru Taormina. Aici am gasit un apartament in comuna pitoreasca Giardini-Naxos, fosta colonie greceasca, aflata la poalele muntelui Tauro. <br><br>
					Pentru un calcul estimativ am cheltuit in jur de 900 RON bilet dus-intors Henri Coanda-Fontanarossa, iar pentru chiria apartamentului 700 RON.
					Odata ajunse in Giardini am inceput sa descoperim frumusetea siciliana: stradute inguste, numarate balcoane impodobite cu teme religioase si marea de azur care mirosea a curatenie. 
					In prima zi am asistat la o ceremonie religioasa dedicata Sfintei Maria in timp ce ne luam masa la un restaurant de pe faleza, urmand sa exploram atractiile principale dupa ce trecea oboseala drumului. Cred ca cele mai bune amintiri ramase sunt cele culinare,gelato cu fistic, pe departe cea mai buna inghetata gustata, canolli, ultima masa a unui mafiot din Il padrino si granita, un fel de suc cu gheata foarte racoritor pentru caldura de acolo. <br><br>
					Am reusit sa facem plimbari la Castelmolla, de unde se poate vedea o priveliste superba, albastrul marii contopindu-se cu cerul, unde am aflat de siesta italienilor,fiind grabite de o vanzatoare de chilipiruri care dorea sa inchida magazinul cat mai repede. 
					In ceea ce priveste alegerea unui restaurant ,sunt numeroase optiunile, in functie de ceea ce doriti sa incercati: pizza, peste, spaghetti, majoritatea felurilor de mancare dintre ele avand ca ingredient complementar fisticul. 
					Daca va aflati in Sicilia nu puteti rata Teatrul Antic, cu o panorama de vis, Isola Bella sau vulcanul Etna. Pe Isola Bella puteti ajunge cu telefericul sau Funivia, iar pe muntele Etna este recomandat sa mergeti printr-o agentie de turism, avand si beneficiul unui ghid care va va povesti istoria acestuia in mai multe limbi. Cea din urma excursie va va costa destul de scump, insa merita toti banii.
					Nu va recomand sa platiti pentru a merge pe o plaja privata, veti gasi pietre in apa si in mare parte aceleasi conditii ca pe o plaja fara plata. 
					De asemenea, este un must plimbarea cu barca care este in jur de 20 de euro, in acest pret intrand racoritoare, o oprire pentru a inota si un istoric al insulei. 
